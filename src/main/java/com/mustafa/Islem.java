package com.mustafa;

public class Islem {
    public int topla(int sayi1, int sayi2) {
        System.out.println("Topla metodu cagrildi");
        return sayi1 + sayi2;
    }

    public int cikart(int sayi1, int sayi2) {
        System.out.println("Cikart metodu cagrildi");
        return sayi1 - sayi2;
    }

    public double bolum(int sayi1, int sayi2) {
        System.out.println("Bolum metodu cagrildi");
        return sayi1 / sayi2;
    }
}
